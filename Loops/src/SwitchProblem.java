public class SwitchProblem {

	public static void main(String[] args) {

		for (int input = 1; input <= 3; input++) {
			switch (input) {
			case 2:
				System.out.println("2");
				// int input = 3;
				break;
			case 1:
				System.out.println("1");
				break;
			default:
				System.out.println("3");
			}

		}

	}
}